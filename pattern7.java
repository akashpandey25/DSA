import java.util.*;
public class pattern7 {
    public static void main(String a[]){
        Scanner sc=new Scanner (System.in);
        int n=sc.nextInt();
        int l=sc.nextInt();
        for(int i=1;i<=n;i++){
            for(int j=1;j<=l;j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
    
}
